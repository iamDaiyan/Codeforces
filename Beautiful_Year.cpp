#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n;
    cin >> n;
    for (int i = n + 1;; i++)
    {
        if (
            ((i % 10000) / 1000) != ((i % 1000) / 100) &&
            ((i % 10000) / 1000) != ((i % 100) / 10) &&
            ((i % 10000) / 1000) != ((i % 10)) &&
            ((i % 1000) / 100) != ((i % 100) / 10) &&
            ((i % 1000) / 100) != ((i % 10)) &&
            ((i % 100) / 10) != ((i % 10))

        )
        {
            cout << i;
            return 0;
        }
    }
}